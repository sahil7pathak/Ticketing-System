<script>
import firebase from "firebase";

export default {
  name: "login",
  data() {
    return {
      username1: null,
      password1: null,
      username2: null,
      password2: null,
      rpassword: null
    };
  },
  methods: {
    validateCredentials(e) {
      const dummy_user = this.username1 + "@npi.com";
      const dummy_pass = this.password1 + "c@npi";
      firebase
        .auth()
        .signInWithEmailAndPassword(dummy_user, dummy_pass)
        .then(
          user => {
            this.$router.go({ path: this.$router.path });
          },
          err => {
            alert(err.message);
          }
        );
      e.preventDefault();
    },
    registerUser() {
      if (this.username2) {
        if (this.password2) {
          if (this.rpassword) {
            if (this.password2 == this.rpassword) {
              const dummy_email = this.username2 + "@npi.com";
              const dummy_passw = this.password2 + "c@npi";
              firebase
                .auth()
                .createUserWithEmailAndPassword(dummy_email, dummy_passw)
                .then(
                  user => {
                    alert("Account Created for " + this.username2);
                    this.$router.go({ path: this.$router.path });
                  },
                  err => {
                    alert(err.message);
                  }
                );
              e.preventDefault();
            } else {
              this.username2 = null;
              this.password2 = null;
              this.rpassword = null;
              alert("Password not matched! Please enter again.");
            }
          } else {
            alert("Please enter the password again");
          }
        } else {
          alert("Enter Password");
        }
      } else {
        alert("Enter Username");
      }
    }
  }
};
</script>

